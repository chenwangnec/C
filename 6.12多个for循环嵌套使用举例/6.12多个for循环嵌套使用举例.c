#include <stdio.h>
int main()
{
	int i, j;
	for (i = 0; i < 3;++i)
	{
		printf("1111!\n");
		for (j = 2; j < 5;++j)
		{
			printf("222!\n");
			printf("333!\n");
		}
		printf("444!\n");
	}
	getchar();
	return 0;
}
