#include <stdio.h>
int main()
{
	int i;
	int sum = 0;
	int cnt = 0;
	for (i = 1; i < 101;i+=2)
	{
		sum += i;//等价于sum=sum+i
		cnt++;//自动加1
	}
	printf("1到100之间奇数的和是:%d\n",sum);
	printf("1到100之间奇数的个数是:%d\n",cnt);
	printf("1到100之间奇数的平均值是:%d\n",sum/cnt);
	getchar();
	return 0;
}
/*
运行结果:
---------------------------------------------
1到100之间奇数的和是:2500
1到100之间奇数的个数是:50
1到100之间奇数的平均值是:50
---------------------------------------------
*/